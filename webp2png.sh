# check depends
function check_prog()
{
	for i in ${1};
	do
		if [ "$(which ${i})" == "" ];
		then
			echo "FATAL: you must install \"${i}\"...";
			return 1;
		fi
	done

	return 0;
}
#---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------#
# general function
function main()
{
# check depends tools
	check_prog "echo cat dwebp mktemp";
	if [ "${?}" != "0" ];
	then
		return 1;
	fi


	if [ "${1}" == "-h" ] || [ "${1}" == "-help" ] || [ "${1}" == "--help" ];
	then
		echo "example: cat FILELIST | ${0}";
		return 1;
	fi


	while read -r OLD_FILE;
	do
		echo "${OLD_FILE}";

		local OLD_FILE_STRIP=$(echo "${OLD_FILE}" | sed -e 's/\..*//g');

		local NEW_FILE="${OLD_FILE_STRIP}.png";

		dwebp "${OLD_FILE}" -o "${NEW_FILE}";
		if [ "${?}" != "0" ];
		then
			echo "ERROR:";
			exit 1;
		fi
	done


	return "${?}";
}
#---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------#
main "${@}";

exit "${?}";
