# check depends
function check_prog()
{
	for i in ${1};
	do
		if [ "$(which ${i})" == "" ];
		then
			echo "FATAL: you must install \"${i}\"...";
			return 1;
		fi
	done

	return 0;
}
#-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------#
# general function
function main()
{
# check minimal depends tools
	check_prog "cp echo";
	if [ "${?}" != "0" ];
	then
		return 1;
	fi


	if [ "${1}" == "-h" ] || [ "${1}" == "-help" ] || [ "${1}" == "--help" ] || [ ! -d "${1}" ];
	then
		echo "example: ls -1 | ${0} DIR";
		return 1;
	fi


	local FILE;
	while read -r FILE;
	do
		if [ -e "${FILE}" ];
		then
			cp -ax "${FILE}" "${1}" &> /dev/null;
			echo "cp -ax ${FILE} ${1}";
		fi
	done


	return 0;
}
#-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------#
main "${@}";

exit "${?}";
