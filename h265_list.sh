# check depends
function check_prog()
{
	for i in ${1};
	do
		if [ "$(command -v ${i})" == "" ];
		then
			echo "FATAL: you must install \"${i}\"...";
			return 1;
		fi
	done

	return 0;
}
#---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------#
# general function
function main()
{
# check depends tools
	check_prog "echo h265.sh";
	if [ "${?}" != "0" ];
	then
		return 1;
	fi


	local FILE;
	while read -r FILE;
	do
		echo "${FILE}";
		h265.sh "${FILE}" "${FILE}.mp4" &> /dev/null < /dev/null;
		if [ "${?}" != "0" ];
		then
			echo "ERROR: broken convert file \"${FILE}\"";
			return 1;
		fi
	done;


	return 0;
}
#---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------#
main "${@}";

exit "${?}";
