# check depends
function check_prog()
{
	for i in ${1};
	do
		if [ "$(command -v ${i})" == "" ];
		then
			echo "FATAL: you must install \"${i}\"...";
			return 1;
		fi
	done

	return 0;
}
#---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------#
# help function
function help()
{
	echo "example: ${1} [ script.sh ]";
}
#---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------#
# general function
function main()
{
# check depends tools
	check_prog "ps grep wc notify_telegram.sh";
	if [ "${?}" != "0" ];
	then
		return 1;
	fi


	if [ "${1}" == "-h" ] || [ "${1}" == "-help" ] || [ "${1}" == "--help" ] || [ "${1}" == "" ];
	then
		help "${0}";
		return 0;
	fi


	local WAIT;
	WAIT="${1}";


	while true;
	do
		local RESULT;
		RESULT=$(ps -fe | grep ${WAIT} | grep -v grep | grep -v wait_run | wc -l);
		if [ "${RESULT}" != "0" ];
		then
			break
		fi


		sleep 1;
	done


	notify_telegram.sh "ok";


	return "${?}";
}
#---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------#
main "${@}";

exit "${?}";
