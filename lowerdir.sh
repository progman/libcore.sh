# check depends
function check_prog()
{
	for i in ${1};
	do
		if [ "$(command -v ${i})" == "" ];
		then
			echo "FATAL: you must install \"${i}\"...";
			return 1;
		fi
	done

	return 0;
}
#---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------#
# general function
function main()
{
# check depends tools
	check_prog "basename dirname echo find mktemp mv rm tr";
	if [ "${?}" != "0" ];
	then
		return 1;
	fi


	if [ "${1}" == "" ] || [ ! -e "${1}" ];
	then
		echo "example: ${0} DIR";
		return 1;
	fi


	TMP="$(mktemp 2> /dev/null)";
	if [ "${?}" != "0" ];
	then
		echo "can't make tmp file";
		return 1;
	fi


	find "${1}" -type f > "${TMP}";
	while read -r LINE;
	do
		DIR="$(dirname "${LINE}")";
		FILE="$(basename "${LINE}")";
		FILE_NEW=$(echo "${FILE}" | tr '[:upper:]' '[:lower:]');

		if [ "${DIR}/${FILE}" != "${DIR}/${FILE_NEW}" ];
		then
			mv "${DIR}/${FILE}" "${DIR}/${FILE_NEW}";
		fi

	done < "${TMP}";


	rm -rf "${TMP}";

	return 0;
}
#---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------#
main "${1}";
exit 0;
