# check depends
function check_prog()
{
	for i in ${1};
	do
		if [ "$(command -v ${i})" == "" ];
		then
			echo "FATAL: you must install \"${i}\"...";
			return 1;
		fi
	done

	return 0;
}
#---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------#
# general function
function main()
{
# check depends tools
	check_prog "echo touch ps grep head";
	if [ "${?}" != "0" ];
	then
		return 1;
	fi


	touch /tmp/xlock_cron


# is lock.sh run?
	local XLOCK_USER=$(ps -fe | grep -- 'xlock.sh' | grep -v grep | { read a b; echo $a; } | head -n 1);

	if [ "${XLOCK_USER}" == "" ];
	then
		exit 0;
	fi


#	XLOCK_USER_HOME=$(getent passwd ${XLOCK_USER} | cut -d: -f6);
#	cd -- "${XLOCK_USER_HOME}";
	su -c 'DISPLAY=":0.0" xset dpms force off' "${XLOCK_USER}";


	return "${?}";
}
#---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------#
main "${@}";

exit "${?}";
