# check depends
function check_prog()
{
	for i in ${1};
	do
		if [ "$(command -v ${i})" == "" ];
		then
			echo "FATAL: you must install \"${i}\"...";
			return 1;
		fi
	done

	return 0;
}
#---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------#
# general function
function main()
{
# check depends tools
	check_prog "echo xrandr grep awk sed";
	if [ "${?}" != "0" ];
	then
		return 1;
	fi


	local WIDTH;
	local X;
	local Y;


	WIDTH=$(xrandr | grep -- '\*\+' | sed -e 's/x.*//g' | sed -e 's/\ *//g');


	X=0;
	Y=0;
	if [ "${WIDTH}" == "1920" ];
	then
		X=1858; # gkrellm width: 50px
		Y=65;
	fi


	if [ "${WIDTH}" == "2560" ];
	then
		X=2497; # gkrellm width: 50px

		if [ "${FLAG_GKRELLM_DOWN}" == "-1" ];
		then
			Y=65;
		fi
		if [ "${FLAG_GKRELLM_DOWN}" == "0" ];
		then
			Y=128;
		fi
		if [ "${FLAG_GKRELLM_DOWN}" == "1" ];
		then
			Y=191;
		fi
	fi


	if [ "${WIDTH}" == "1366" ]; # 1366x768
	then
		X=1303; # gkrellm width: 50px

		if [ "${FLAG_GKRELLM_DOWN}" == "-1" ];
		then
			Y=65;
		fi
		if [ "${FLAG_GKRELLM_DOWN}" == "0" ];
		then
			Y=128;
		fi
		if [ "${FLAG_GKRELLM_DOWN}" == "1" ];
		then
			Y=191;
		fi
	fi


	gkrellm --geometry "+${X}+${Y}"


	return 0;
}
#---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------#
main "${@}";

exit "${?}";
