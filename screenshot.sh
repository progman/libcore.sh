# check depends
function check_prog()
{
	for i in ${1};
	do
		if [ "$(command -v ${i})" == "" ];
		then
			echo "FATAL: you must install \"${i}\"...";
			return 1;
		fi
	done

	return 0;
}
#---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------#
# general function
function main()
{
# check depends tools
	check_prog "date echo import mktemp mv rm";
	if [ "${?}" != "0" ];
	then
		return 1;
	fi


	WINDOW='root';
	if [ "${1}" != "" ];
	then
		WINDOW="${1}";
	fi

	FILENAME="/tmp/screenshot-$(date '+%Y%m%d_%H%M%S').png";


	if [ "$(command -v import)" == "" ];
	then
		echo "ERROR: imagemagick not found";
		return 1;
	fi


	import -window "${WINDOW}" "${FILENAME}";


	if [ "$(command -v pngcrush)" != "" ];
	then
		TMP="$(mktemp 2> /dev/null)";
		if [ "${?}" != "0" ];
		then
			echo "FATAL: can't make tmp file";
			return 1;
		fi

		pngcrush -brute -l 9 "${FILENAME}" "${TMP}" &> /dev/null;
		if [ "${?}" == "0" ];
		then
			mv "${TMP}" "${FILENAME}";
		else
			rm -rf "${TMP}";
		fi
	fi

	if [ "$(command -v beep)" != "" ];
	then
		beep;
	fi

	return 0;
}
#---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------#
main "${@}";

exit "${?}";
