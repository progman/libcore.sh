# check depends
function check_prog()
{
	for i in ${1};
	do
		if [ "$(command -v ${i})" == "" ];
		then
			echo "FATAL: you must install \"${i}\"...";
			return 1;
		fi
	done

	return 0;
}
#---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------#
# general function
function main()
{
# check depends tools
	check_prog "echo dd";
	if [ "${?}" != "0" ];
	then
		return 1;
	fi


	local OFFSET;
	OFFSET="${1}";


	if ! [ "${OFFSET}" -eq "${OFFSET}" ] 2> /dev/null
	then
		echo "example: cat FILE | ${0} OFFSET";
		return 0;
	fi


	dd if=/dev/stdin of=/dev/stdout skip="${OFFSET}" bs=1 2> /dev/null


	return 0;
}
#---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------#
main "${@}";

exit "${?}";
