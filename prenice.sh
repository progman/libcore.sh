# check depends
function check_prog()
{
	for i in ${1};
	do
		if [ "$(command -v ${i})" == "" ];
		then
			echo "FATAL: you must install \"${i}\"...";
			return 1;
		fi
	done

	return 0;
}
#---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------#
# general function
function main()
{
# check depends tools
	check_prog "echo ps grep awk renice";
	if [ "${?}" != "0" ];
	then
		return 1;
	fi


	if [ "${UID}" != "0" ];
	then
		echo "You NOT root !";
		return 1;
	fi


	if [ "${1}" == "" ];
	then
		echo "example: prenice -20 bash"
		return 1;
	fi
	if [ "${2}" == "" ];
	then
		echo "example: prenice -20 bash"
		return 1;
	fi


	for PID in `ps -A | grep "${2}" | awk '{print $1}'`;
	do
		renice "${1}" "${PID}";
	done

	return 0;
}
#---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------#
main "${@}";

exit "${?}";
