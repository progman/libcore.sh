# check depends
function check_prog()
{
	for i in ${1};
	do
		if [ "$(command -v ${i})" == "" ];
		then
			echo "FATAL: you must install \"${i}\"...";
			return 1;
		fi
	done

	return 0;
}
#---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------#
# general function
function main()
{
# check depends tools
	check_prog "echo cat head tail";
	if [ "${?}" != "0" ];
	then
		return 1;
	fi


	if  [ "${1}" == "-h" ] || [ "${1}" == "-help" ] || [ "${1}" == "--help" ];
	then
		echo "example: ${0} SOURCE_FILE INDEX";
		return 0;
	fi


	local FILENAME;
	FILENAME="${1}";

	local INDEX;
	INDEX="${2}";


	if [ ! -e "${FILENAME}" ];
	then
		echo "example: ${0} SOURCE_FILE INDEX";
		return 0;
	fi



	if [ "$(echo ${INDEX} | sed -e 's/[0-9]*//g' | sed -e '/^$/d' | wc -l)" != "0" ];
	then
		echo "example: ${0} SOURCE_FILE INDEX";
		return 0;
	fi


	(( INDEX++ ));



	cat "${FILENAME}" | head -n "${INDEX}" | tail -n 1


	return 0;
}
#---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------#
main "${@}";

exit "${?}";
