# check depends
function check_prog()
{
	for i in ${1};
	do
		if [ "$(command -v ${i})" == "" ];
		then
			echo "FATAL: you must install \"${i}\"...";
			return 1;
		fi
	done

	return 0;
}
#---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------#
# general function
function main()
{
# check depends tools
	check_prog "echo sqlite3";
	if [ "${?}" != "0" ];
	then
		return 1;
	fi


	local SOURCE;
	SOURCE="${1}";
	if [ ! -f "${SOURCE}" ];
	then
		echo "example: ${0} SOURCE";
		return 0;
	fi


	local TARGET;
	TARGET="${SOURCE}.sql";
	echo '.dump' | sqlite3 "${SOURCE}" > "${TARGET}";


	return 0;
}
#---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------#
main "${@}";

exit "${?}";
