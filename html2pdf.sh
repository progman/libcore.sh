# check depends
function check_prog()
{
	for i in ${1};
	do
		if [ "$(command -v ${i})" == "" ];
		then
			echo "$(get_time)! FATAL: you must install \"${i}\", exit";
			return 1;
		fi
	done

	return 0;
}
#---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------#
# general function
function main()
{
	local STATUS;


	local ARGC="${#}";
	if [ "${ARGC}" != "1" ] && [ "${ARGC}" != "2" ];
	then
		echo "example: ${0} SOURCE.HTML [TARGET.PDF]";
		return 1;
	fi


	local FILE_SOURCE="${1}";
	local FILE_TARGET="${2}";


	if [ "${ARGC}" == "1" ];
	then
		FILE_TARGET="${FILE_SOURCE}.pdf";
	fi


# check minimal depends tools
	check_prog "echo ps wc google-chrome";
	if [ "${?}" != "0" ];
	then
		return 1;
	fi


# add into html:
#
#<style type="text/css" media="print">
#@page {
#    size:   auto;  /* auto is the initial value */
#    margin: 0;     /* this affects the margin in the printer settings */
#}
#</style>


# convert pdf
	google-chrome --no-sandbox --headless --disable-gpu --no-margins --print-to-pdf-no-header --print-to-pdf="${FILE_TARGET}" "${FILE_SOURCE}";
	if [ "${?}" != "0" ]
	then
		return 1;
	fi


	return 0;
}
#---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------#
main "${@}";

exit "${?}";
