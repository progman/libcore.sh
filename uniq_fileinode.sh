# check depends
function check_prog()
{
	for i in ${1};
	do
		if [ "$(command -v ${i})" == "" ];
		then
			echo "FATAL: you must install \"${i}\"...";
			return 1;
		fi
	done

	return 0;
}
#---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------#
# view help
function help()
{
	echo "example: cat FILELIST | ${0} [-r]";
}
#---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------#
# general function
function main()
{
	if [ "${1}" == "--help" ] || [ "${1}" == "-help" ] || [ "${1}" == "-h" ];
	then
		help;
		return 1;
	fi


# check depends tools
	check_prog "echo grep wc stat sort";
	if [ "${?}" != "0" ];
	then
		return 1;
	fi


	while read -r FILE;
	do
		if [ -e "${FILE}" ];
		then
			stat --format '%i %n' "${FILE}";
		fi
	done | sort -n |
	{
		INODE_OLD='';
		while read INODE FILENAME;
		do
			if [ "${INODE}" != "${INODE_OLD}" ];
			then
				echo "${FILENAME}";
			fi
			INODE_OLD="${INODE}";
		done
	}

	return 0;
}
#---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------#
main "${@}";

exit "${?}";
