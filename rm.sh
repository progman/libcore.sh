# check depends
function check_prog()
{
	for i in ${1};
	do
		if [ "$(command -v ${i})" == "" ];
		then
			echo "FATAL: you must install \"${i}\"...";
			return 1;
		fi
	done

	return 0;
}
#---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------#
# general function
function main()
{
# check minimal depends tools
	check_prog "rm echo";
	if [ "${?}" != "0" ];
	then
		return 1;
	fi


	if [ "${1}" == "-h" ] || [ "${1}" == "-help" ] || [ "${1}" == "--help" ];
	then
		echo "example: ls -1 | ${0}";
		return 1;
	fi


	local FILE;
	while read -r FILE;
	do
		if [ -e "${FILE}" ] || [ -L "${FILE}" ];
		then
			rm -rf "${FILE}" &> /dev/null;
			if [ "${?}" == "0" ];
			then
#				echo "rm -rf \"${FILE}\"";
				echo "${FILE}";
			fi
		fi
	done


	return 0;
}
#---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------#
main "${@}";

exit "${?}";
