# check depends
function check_prog()
{
	for i in ${1};
	do
		if [ "$(which ${i})" == "" ];
		then
			echo "FATAL: you must install \"${i}\"...";
			return 1;
		fi
	done

	return 0;
}
#---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------#
# general function
function main()
{
# check minimal depends tools
	check_prog "echo mv";
	if [ "${?}" != "0" ];
	then
		return 1;
	fi


	if [ "${1}" == "-h" ] || [ "${1}" == "-help" ] || [ "${1}" == "--help" ];
	then
		echo "example: ls -1 | ${0}";
		return 1;
	fi


	local SOURCE;
	local TARGET;
	while read -r SOURCE;
	do
		if [ -e "${SOURCE}" ] || [ -L "${SOURCE}" ];
		then
			TARGET=$(echo "${SOURCE}" | iconv -fcp1251);

			mv -- "${SOURCE}" "${TARGET}" &> /dev/null;
			if [ "${?}" == "0" ];
			then
				echo "${SOURCE}";
			fi
		fi
	done


	return 0;
}
#---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------#
main "${@}";

exit "${?}";
